    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        request.setCharacterEncoding("UTF-8");
        response.setContentType("text/html; charset=UTF-8");
        response.setCharacterEncoding("UTF-8");

        String fullName = request.getParameter("fullName");
        String phoneNumber = request.getParameter("phoneNumber");
        String gender = request.getParameter("gender");
        String[] language = request.getParameterValues("language");
        String duration = request.getParameter("course");
        String cmt = request.getParameter("comment");

        response.getWriter().println("<h1>Details Entered</h1>");
        response.getWriter().println("Full Name: " + fullName + "<br>");
        response.getWriter().println("Phone Number: " + phoneNumber + "<br>");
        response.getWriter().println("Gender: " + gender + "<br>");
        response.getWriter().println("Programming languages selected: " + Arrays.toString(language) + "<br>");
        response.getWriter().println("Duration of course: " + duration + "<br>");
        response.getWriter().println("Comments: " + cmt + "<br>");
    }

